package fundamentos.operadores;

public class Aritmeticos {
	public static void main(String[] args) {
		
		var x = 22;
		var y = 3;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
		System.out.println(x /(double) y);
		System.out.println(x % y);

	}
}
